#include <iostream>

int main() {
    // Printing the tree first
    std::cout << "   *   " << std::endl;
    std::cout << "  ***  " << std::endl;
    std::cout << " ***** " << std::endl;
    std::cout << "*******" << std::endl;
    std::cout << "  ***  " << std::endl;

    // Creating two blank lines
    std::cout << std::endl << std::endl;

    // Printing the cat after
    std::cout << "/\\   /\\" << std::endl;
    std::cout << "  o o    " << std::endl;
    std::cout << " =   =   " << std::endl;
    std::cout << "  ---    " << std::endl;

    return 0;
}
